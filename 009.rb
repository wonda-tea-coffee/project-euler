# a^2 + b^2 = c^2
# a + b + c = 1000
# c = 1000 - a - b
# a^2 + b^2 = {1000 - (a + b)}^2
# a^2 + b^2 = 1_000_000 - 2000(a + b) + (a + b)^2
# a^2 + b^2 = 1_000_000 - 2000(a + b) + a^2 + 2ab + b^2
# -ab + 1000(a + b) = 500_000
# 1000b -ab = 500_000 - 1_000a
# b(1000 - a) = 500_000 - 1_000a
# b = 1000(500 - a) / (1000 - a)

(1...500).each do |a|
  b = 1000 * (500 - a) / (1000 - a)
  c = Math.sqrt(a**2 + b**2)
  if a + b + c == 1000
    puts a*b*c.to_i
    break
  end
end
